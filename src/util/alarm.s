;------------------------------------------------------------------------------
; private bool alarm_fired : u8;
;------------------------------------------------------------------------------
; Description: Dictates whether or not the timer has fired yet or not
;------------------------------------------------------------------------------
alarm_fired:
.space 1



;------------------------------------------------------------------------------
; void AlarmInit(void)
;------------------------------------------------------------------------------
; Description: Initializes the alarm.
;------------------------------------------------------------------------------
; Parameters:
; None.
;------------------------------------------------------------------------------
; Returns:
; None.
;------------------------------------------------------------------------------

VideoGraphInit:
    ldr r0, =alarm_fired
    mov r1, #0
    str r1, [r0]

    ldr r0, =AlarmIRQHandler
    ldr r1, =IRQ_VECTOR
    str r1, [r0]
    bx lr